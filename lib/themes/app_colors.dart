  static Color getStatusColor(String status, BuildContext context) {
    switch (status.toLowerCase()) {
      case 'active':
      case 'verified':
      case 'subscribed':
      case 'completed':
        return success;
      case 'pending':
      case 'coming_soon':
      case 'in_progress':
        return statusPending;
      case 'rejected':
      case 'locked':
      case 'expired':
      case 'cancelled':
        return error;
      case 'free':
        return telegramBlue;
      default:
        return getTextSecondary(context);
    }
  }

